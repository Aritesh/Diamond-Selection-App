import '../../domain/entities/diamond.dart';

class DiamondModel extends Diamond {
  const DiamondModel({
    required String lotId,
    required double size,
    required double carat,
    required String lab,
    required String shape,
    required String color,
    required String clarity,
    required String cut,
    required String polish,
    required String symmetry,
    required String fluorescence,
    required double discount,
    required double perCaratRate,
    required double finalAmount,
    required String keyToSymbol,
    required String labComment,
  }) : super(
         lotId: lotId,
         size: size,
         carat: carat,
         lab: lab,
         shape: shape,
         color: color,
         clarity: clarity,
         cut: cut,
         polish: polish,
         symmetry: symmetry,
         fluorescence: fluorescence,
         discount: discount,
         perCaratRate: perCaratRate,
         finalAmount: finalAmount,
         keyToSymbol: keyToSymbol,
         labComment: labComment,
       );

  factory DiamondModel.fromJson(Map<String, dynamic> json) => DiamondModel(
    lotId: json['lotId'],
    size: json['size'],
    carat: json['carat'],
    lab: json['lab'],
    shape: json['shape'],
    color: json['color'],
    clarity: json['clarity'],
    cut: json['cut'],
    polish: json['polish'],
    symmetry: json['symmetry'],
    fluorescence: json['fluorescence'],
    discount: json['discount'],
    perCaratRate: json['perCaratRate'],
    finalAmount: json['finalAmount'],
    keyToSymbol: json['keyToSymbol'],
    labComment: json['labComment'],
  );

  Map<String, dynamic> toJson() => {
    'lotId': lotId,
    'size': size,
    'carat': carat,
    'lab': lab,
    'shape': shape,
    'color': color,
    'clarity': clarity,
    'cut': cut,
    'polish': polish,
    'symmetry': symmetry,
    'fluorescence': fluorescence,
    'discount': discount,
    'perCaratRate': perCaratRate,
    'finalAmount': finalAmount,
    'keyToSymbol': keyToSymbol,
    'labComment': labComment,
  };
}
